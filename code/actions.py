import  math

class operators:
    
    def add(self,x,y):
        return x+y

    def sub(self,x,y):
        return x-y
    
    def multi(self,x,y):
        return x*y
    
    def mod(self,x,y):
        return x%y

    def div(self,x,y):
        return x/y

    def power(self,x,y):
        return x**y

    def square(self,x):
        return x*x

    def cube(self,x):
        return x**3

    def sqrt(self,x):
        return x**(0.5)

    def cubrt(self,x):
        return x*(1/3)

    def sine(self,x):
        return math.sin(x)
        
    def cosine(self,x):
        return math.cos(x)

    def tangent(self,x):
        return math.tan(x)    
